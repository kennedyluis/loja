<p>Inobstante às doações avulsas, a Diretoria de nossa Oficina, através de nosso Venerável Mestre – Irmão João Mendes – e de nosso Presidente da Comissão de Obras – Irmão Adail Rodrigues – criaram e estão implementando o Projeto <strong>CONSTRUTORES DO TEMPLO DA VIRTUDE.</strong></p>
<p>Através dele, o Irmão interessado em nos ajudar escolhe uma classe de doação, em módicas parcelas mensais, que nos auxiliarão no custeio para finalização das obras, definindo, ainda, a melhor data para iniciar suas doações.</p>
<p><strong>As classes de doadores do Projeto são as seguintes:</strong></p>
<p>Classe 1 - <strong>*Obreiro Fiel*</strong> - Doará 12 parcelas mensais de R$ 50,00... receberá, além de nosso carinho e respeito, um certificado de agradecimento e reconhecimento relativo a esta classe e uma camisa comemorativa;</p>
<p>Classe 2 - <strong>*Intendente de Obras*</strong> - Fará a doação de 12 parcelas mensais de R$ 80,00... fará, então, jus, além de nosso carinho, respeito e consideração, o certificado relativamente à esta classe, a camisa comemorativa e uma comenda de honra;</p>
<p>Classe 3 - <strong>*Arquiteto Geral*</strong> - Doação de 12 parcelas mensais de R$ 120,00... receberá certificado relativo à classe, à camisa alusiva, a comenda e terá uma lajota de agradecimento afixada com seus dados pessoais em uma das paredes da sala dos passos perdidos da Loja.</p>
<p>Já alcançamos, praticamente, metade de nossa meta, mediante adesão de valorosos Irmãos de vários Estados do Brasil e do Exterior, a quem agradecemos a confiança deposita e o apoio abnegado.</p>
<p>Além disso, nosso Projeto está sendo atendido fraternalmente, já contando com a participação de Irmãos da CMSB, GOB e COMAB, além de Potências estrangeiras reconhecidas, comprovando o Amor Incondicional que nos unem.</p>
