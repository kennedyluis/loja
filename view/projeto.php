<p>Nossa Loja, atualmente, funciona de forma provisória em Rio Branco/AC, mas temos o compromisso moral de concluirmos a edificação de nossa sede e precisamos nos mudar para nosso Oriente de origem, a fim de que possamos difundir e ampliar nossos trabalhos sociais, filantrópicos e maçônicos para aquela comunidade carente.</p>
<p>Bujari é uma pequena e humilde cidade do interior do Estado do Acre, distante 25 Km da capital, com aproximadamente 10.000 habitantes e nossa presença, com certeza, a engrandecerá e trará Luz e Progresso Social.</p>
