<h3><strong><span class="glyphicon glyphicon-asterisk"></span> O ESTANDARTE</strong></h3><br>
<div class="col-md-6">
<p>O teu azul representa o céu infinito do nosso sistema, ofuscante de luares mágicos e mune de fartura e de amor, fonte inesgotável de alento e de beleza.</p> 
<p>O teu branco, representado pela “Pomba”, simboliza a paz dos nossos sonhos, de um dia ver toda humanidade se confraternizando com o mesmo objetivo buscando a paz. Sem preconceito de raça, cor, religião e de riqueza.</p> 
<p>O teu verde, representado pelo ramos de Acácia, árvore forte, resistente ao calor, a chuva, ao frio e às tempestades, está imortalizada na Maçonaria. O verde também cor da esperança, perpetuando a imensa floresta Amazônica, símbolo maior do nosso Pais e patrimônio Universal.</p> 
<p>O teu amarelo representa o ouro, riqueza do nosso subsolo, e o sol que nos ilumina e excita. Pai dos nossos sonhos, ofuscante com seus raios de vida.</p> 
<p>O teu pavimento de Mosaico, de cores branco e preto, simboliza a união de todos os maçons, apesar da diversidade de cores e de raças, o antagonismo das religiões, e dos princípios que regem os diferentes povos, e todas as coisas da natureza. 
Nos capitéis de nossas colunas “J” e “B” estão as romãs, simbolizando a união indissolúvel dos povos maçônicos, espalhados em toda a superfície da terra. E sob o pavimento de mosaico estão as ferramentas de trabalhos do maçom. O cinzel, a colher de pedreiro e o maço, simbolizando os Obreiros da Construção da Paz Universal.</p><br>
